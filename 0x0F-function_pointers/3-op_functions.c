#include "3-cal.h"
#include <stdio.h>
#include <stdlib.h>

/**
 * op_add - entry point
 * @a: first input
 * @b: second input
 * Return: difference
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - entry point
 * @a: first input
 * @b: second input
 * Return: differrence
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - entry point
 * @a: first input
 * @b: second input
 * Return: difference
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - entry point
 * @a: first input
 * @b: second input
 * Return: difference
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - entry point
 * @a: first input
 * @b: second input
 * Return: different
 */
int op_mod(int a, int b)
{
	return (a % b);
}
